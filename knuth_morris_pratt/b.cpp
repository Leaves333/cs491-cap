vi prefix_function(string s) {
    int n = s.length();
    vi pi(n);
    for (int i = 1; i < n; i++) {
        int j = pi[i-1];
        while (j > 0 && s[i] != s[j])
            j = pi[j-1];
        if (s[i] == s[j])
            j++;
        pi[i] = j;
    }
    return pi;
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    string s; cin >> s;
    vi p = prefix_function(s);
    cout << s.substr(0, s.length() - p.back()) << endl;

}
